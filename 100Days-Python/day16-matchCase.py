# MATCH CASE STATEMENTS

x = int(input("Numbner: "))

match x:
    case 1:
        print("It is SUNDAY")
    case 2:
        print("It is MONDAY")
    case 3:
        print("It is TUESDAY")
    case 4:
        print("It is WEDNESDAY")
    case 5:
        print("It is THURSDAY")
    case 6:
        print("It is FRIDAY")
    case 7:
        print("It is SATURDAY")

